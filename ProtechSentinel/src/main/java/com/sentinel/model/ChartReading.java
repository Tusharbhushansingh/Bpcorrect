package com.sentinel.model;

public class ChartReading {

String date;
String avg_values;
String actual_values;
public String getDate() {
	return date;
}
public void setDate(String date) {
	this.date = date;
}
public String getAvg_values() {
	return avg_values;
}
public void setAvg_values(String avg_values) {
	this.avg_values = avg_values;
}
public String getActual_values() {
	return actual_values;
}
public void setActual_values(String actual_values) {
	this.actual_values = actual_values;
}


}
